def perk(n, d, i, t):
    return "{n:'" + n + "',d:'" + d + "',i:'" + i + "',t:'" + t + "'}"

def branch(name, perk_list):
    p = ", ".join(perk(*x) for x in perk_list)
    return "{name:'" + name + "',perks:[" + p + "]}"

def tier(ti, branches):
    bs = ", ".join(branch(*x) for x in branches)
    return "{tier:" + str(ti) + ",branches:[" + bs + "]}"

def attr(key, name, color, tiers):
    return ("{key:'" + key + "',name:'" + name + "',color:'" + color + "',\n"
            + " tiers:[\n  " + ",\n  ".join(tiers) + "\n"
            + " ]}")

